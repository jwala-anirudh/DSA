package PrePlacement.Day5;

/**
 * Rotate matrix by 90 degrees
 * <p>
 * Transpose + Reverse row-wise
 */
public class Problem03 {
    void rotate(int[][] matrix) {
        transpose(matrix);
        mirrorReflect(matrix);
    }

    void transpose(int[][] matrix) {
        int n = matrix.length;
        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                int temp = matrix[j][i];
                matrix[j][i] = matrix[i][j];
                matrix[i][j] = temp;
            }
        }
    }

    void mirrorReflect(int[][] matrix) {
        int n = matrix.length;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n / 2; j++) {
                int temp = matrix[i][j];
                matrix[i][j] = matrix[i][n - j - 1];
                matrix[i][n - j - 1] = temp;
            }
        }
    }
}
